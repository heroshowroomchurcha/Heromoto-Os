-- 1. Create Core Tables
CREATE TABLE IF NOT EXISTS public.inventory (
  id bigint generated by default as identity primary key,
  model text not null,
  variant text,
  cc integer,
  color text,
  ex_showroom_price numeric,
  on_road_price numeric,
  max_discount numeric,
  stock integer not null default 1,
  stock_by_color jsonb default '[]'::jsonb,
  created_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.customers (
  id bigint generated by default as identity primary key,
  name text not null,
  phone text,
  aadhaar_last4 text,
  pan_masked text,
  aadhaar_document_path text,
  pan_document_path text,
  created_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.sales (
  id bigint generated by default as identity primary key,
  customer_id bigint references public.customers(id),
  customer_name text not null,
  customer_phone text,
  vehicle_id bigint references public.inventory(id),
  vehicle_name text not null,
  original_price numeric,
  discount numeric,
  sale_amount numeric,
  payment_status text,
  sale_type text default 'Sale',
  old_vehicle_name text,
  old_vehicle_registration text,
  exchange_discount numeric default 0,
  exchange_value numeric default 0,
  emi_amount numeric,
  tenure_months integer,
  amount_paid numeric default 0,
  signature_data text,
  invoice_number text,
  rc_photo_path text,
  sale_date timestamptz default now(),
  updated_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.test_drive_inventory (
  id bigint generated by default as identity primary key,
  model text not null,
  variant text,
  cc integer,
  color text,
  stock integer not null default 1,
  status text not null default 'Available',
  created_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.test_drives (
  id bigint generated by default as identity primary key,
  customer_name text not null,
  phone_number text,
  vehicle_id bigint references public.test_drive_inventory(id),
  status text default 'Scheduled',
  date date,
  time text,
  license_number text,
  notes text,
  created_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.rc_records (
  id bigint generated by default as identity primary key,
  sale_id bigint references public.sales(id),
  customer_name text not null,
  customer_phone text,
  vehicle_name text not null,
  status text default 'Pending',
  completed_at timestamptz,
  created_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  role text not null default 'staff' check (role in ('owner', 'staff')),
  created_at timestamptz default now()
);

