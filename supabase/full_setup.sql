-- 6. Storage Bucket
insert into storage.buckets (id, name, public)
values ('customer-documents', 'customer-documents', false)
on conflict (id) do update set public = false;

drop policy if exists "Staff upload customer documents" on storage.objects;
drop policy if exists "Staff read customer documents" on storage.objects;
drop policy if exists "Staff delete customer documents" on storage.objects;
create policy "Staff upload customer documents" on storage.objects for insert to authenticated
with check (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);
create policy "Staff read customer documents" on storage.objects for select to authenticated
using (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);
create policy "Staff delete customer documents" on storage.objects for delete to authenticated
using (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);

