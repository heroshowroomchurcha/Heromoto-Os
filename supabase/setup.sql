-- Private KYC document storage. Files are never public; authenticated staff
-- can only access files inside their own user-id folder.
insert into storage.buckets (id, name, public)
values ('customer-documents', 'customer-documents', false)
on conflict (id) do update set public = false;

drop policy if exists "Staff upload customer documents" on storage.objects;
drop policy if exists "Staff read customer documents" on storage.objects;
drop policy if exists "Staff delete customer documents" on storage.objects;
create policy "Staff upload customer documents" on storage.objects for insert to authenticated
with check (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);
create policy "Staff read customer documents" on storage.objects for select to authenticated
using (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);
create policy "Staff delete customer documents" on storage.objects for delete to authenticated
using (bucket_id = 'customer-documents' and (storage.foldername(name))[1] = auth.uid()::text);

-- STAFF AUTH + ROLES
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  role text not null default 'staff' check (role in ('owner', 'staff')),
  created_at timestamptz default now()
);
alter table public.profiles enable row level security;

create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = public
as $$ begin
  insert into public.profiles (id, full_name)
  values (new.id, coalesce(new.raw_user_meta_data->>'full_name', new.email));
  return new;
end; $$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created after insert on auth.users
for each row execute procedure public.handle_new_user();

drop policy if exists "Profiles self read" on public.profiles;
drop policy if exists "Owner reads profiles" on public.profiles;
create policy "Profiles self read" on public.profiles for select to authenticated using (id = auth.uid());

create or replace function public.is_owner()
returns boolean language sql security definer set search_path = public
as $$ select exists (select 1 from public.profiles where id = auth.uid() and role = 'owner'); $$;

-- Avoid querying profiles from inside its own RLS policy. The security-definer
-- helper checks the current authenticated user's role safely.
drop policy if exists "Owner reads profiles" on public.profiles;
create policy "Owner reads profiles" on public.profiles for select to authenticated using (
  id = auth.uid() or public.is_owner()
);

