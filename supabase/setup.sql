-- Dedicated bikes reserved for customer test drives. This is intentionally
-- separate from sale inventory, so a showroom can keep 4-5 demo units aside.
create table if not exists public.test_drive_inventory (
  id bigint generated by default as identity primary key,
  model text not null,
  variant text,
  cc integer,
  color text,
  stock integer not null default 1,
  status text not null default 'Available',
  created_at timestamptz default now()
);
alter table public.test_drive_inventory enable row level security;
drop policy if exists "MVP read test drive inventory" on public.test_drive_inventory;
drop policy if exists "MVP write test drive inventory" on public.test_drive_inventory;
create policy "MVP read test drive inventory" on public.test_drive_inventory for select to authenticated using (true);
create policy "MVP write test drive inventory" on public.test_drive_inventory for all to authenticated using (true) with check (true);

