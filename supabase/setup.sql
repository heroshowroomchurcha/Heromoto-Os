-- Backfill customers and RC records for any sales already entered before this setup.
insert into public.customers (name, phone, created_at)
select distinct on (s.customer_name) s.customer_name, s.customer_phone, s.sale_date
from public.sales s
where not exists (
  select 1 from public.customers c where c.name = s.customer_name and coalesce(c.phone, '') = coalesce(s.customer_phone, '')
);

insert into public.rc_records (sale_id, customer_name, vehicle_name, status, created_at)
select s.id, s.customer_name, s.vehicle_name, 'Pending', s.sale_date
from public.sales s
where not exists (
  select 1 from public.rc_records r where r.sale_id = s.id
);

